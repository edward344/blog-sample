<p class="text-justify">Vegetarianism is the practice of abstaining from the consumption of meat (red meat, poultry
, seafood, and the 
flesh of any other animal), and may also include abstention from by-products of animal slaughter.

Vegetarianism may be adopted for various reasons. Many people object to eating meat out of respect for sentient 
life. Such ethical motivations have been codified under various religious beliefs, as well as animal rights 
advocacy.
 Other motivations for vegetarianism are health-related, political, environmental, cultural, aesthetic, economic, 
 or personal preference. There are variations of the diet as well: an ovo-lacto vegetarian diet includes both eggs 
 and dairy products, an ovo-vegetarian diet includes eggs but not dairy products, and a lacto-vegetarian diet 
 includes dairy products but not eggs. A strict vegetarian diet – referred to as vegan – excludes all animal 
 products, including eggs and dairy. Avoidance of animal products requires dietary supplemental vitamin B12 from 
fortified manufactured foods or supplement products to avoid vitamin B12 deficiency, which leads to high
 homocysteine blood levels – a risk factor for several chronic health disorders.
Packaged and processed foods, such as cakes, cookies, candies, chocolate, yogurt, and marshmallows, often contain 
unfamiliar animal ingredients, so may be a special concern for vegetarians due to the likelihood of such additions.
Often, prior to purchase or consumption, vegetarians will scrutinize products for animal-derived ingredients.
 Vegetarians' feelings vary with regard to these ingredients. For example, while some vegetarians may be unaware
 of animal-derived rennet's role in the production of cheese, and may therefore unknowingly consume the product,
 [6][7] other vegetarians may not take issue with its consumption.
Semi-vegetarian diets consist largely of vegetarian foods but may include fish or poultry, or sometimes other 
meats,
 on an infrequent basis. Those with diets containing fish or poultry may define meat only as mammalian flesh and
 may
 identify with vegetarianism. A pescetarian diet has been described as "fish but no other meat".The 
 common-use association between such diets and vegetarianism has led vegetarian groups such as the Vegetarian
 Society to state that diets containing these ingredients are not vegetarian, because fish and birds are also
 animals</p>
 
